<section class="histoire fade-in">
  <p>
    Né en 2025, EduLearn est le fruit d’un projet d’évaluation proposé aux étudiants de P2B, vacation soir, de l’Institut Supérieur d’Informatique, Programmation et Analyse (ISIPA). 
    Ce défi académique visait à mesurer leur niveau de maîtrise des langages HTML, CSS et PHP à travers la création d’une plateforme éducative fonctionnelle et moderne.
  </p>
  <p>
    Plus qu’un simple exercice, ce projet est rapidement devenu une initiative ambitieuse : rendre l’apprentissage plus accessible, interactif et personnalisé, tout en valorisant les compétences techniques des étudiants.
  </p>
